    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String username = txtUserName.getText().trim();
        char[] passwrd = txtPass.getPassword();
        String userpass = String.valueOf(passwrd).trim();
        if (username.equals("") || userpass.equals("")) {
            JOptionPane.showMessageDialog(this, "Kullanıcı bilgileri boş bırakılamaz !");
        } else {
            try {
                String query = " SELECT * FROM `admin` WHERE `username` = ? AND `userpass` = ? ";
                PreparedStatement pre = db.preBaglan(query);
                pre.setString(1, username);
                pre.setString(2, userpass);
                ResultSet rs = pre.executeQuery();
                if (rs.next()) {
                    Dashboard dh = new Dashboard();
                    dh.setVisible(true);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Kullanıcı Bilgileri Hatalı!");
                }
            } catch (Exception e) {
                System.err.println("Login error " + e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

